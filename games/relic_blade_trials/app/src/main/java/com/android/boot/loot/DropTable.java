package com.android.boot.loot;

public class DropTable {
    public final float normalWhite;
    public final float normalGreen;
    public final float bossGreen;
    public final float bossPurple;
    public final float bossGold;

    public DropTable(float normalWhite, float normalGreen, float bossGreen, float bossPurple, float bossGold) {
        this.normalWhite = normalWhite;
        this.normalGreen = normalGreen;
        this.bossGreen = bossGreen;
        this.bossPurple = bossPurple;
        this.bossGold = bossGold;
    }
}
