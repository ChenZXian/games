package com.android.boot.fx;

public class Particle {
    public float x;
    public float y;
    public float speed;
    public float size;
    public float alpha;

    public Particle(float x, float y, float speed, float size, float alpha) {
        this.x = x;
        this.y = y;
        this.speed = speed;
        this.size = size;
        this.alpha = alpha;
    }
}
